package core.http.exception;

/**
 * Represents a wrapper used to distinguish max redirects errors from others The
 */
public class HttpMaxRedirectsException extends HttpException {

	private static final long serialVersionUID = 5594002276632915175L;

	/**
	 * Constructor
	 * @param message
	 */
	public HttpMaxRedirectsException(Object message) {
		super(CODE_301, null, message, null);
	}
}