package core.lang.basic;

/**
 * A Basic Index Array.
 */
public interface BasicIndexArray extends BasicArray {

	/**
	 * Add the given index.
	 * @param index the index.
	 */
	void add(int index);

	/**
	 * Returns the index at the given index!
	 * @param index the index.
	 * @return the index!
	 */
	int get(int index);

	/**
	 * Returns the indexes as an array.
	 * @return the indexes as an array.
	 */
	int[] toArray();

	/**
	 * Swap the values at the given indexes.
	 * @param index1 the first index.
	 * @param index2 the second index.
	 */
	void swap(int index1, int index2);

	/**
	 * Sort the array.
	 * @param ascending true to sort in ascending order
	 */
	void sort(boolean ascending);

	/**
	 * Remove duplicate values.
	 */
	void removeDuplicates();
}
