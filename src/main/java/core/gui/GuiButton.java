package core.gui;

import java.awt.event.ActionListener;

import javax.swing.JButton;

/**
 * A GUI Button.
 */
public class GuiButton extends JButton {

	/**
	 * Creates a new button.
	 * @param text the text.
	 */
	public GuiButton(String text, ActionListener listener) {
		super(text);
		addActionListener(listener);
	}

}
