package core.html.field;

/**
 * A Text Field.
 */
public final class FieldText extends FieldInput {

	/**
	 * Creates a new input.
	 * @return a new input.
	 */
	protected final FieldInput newInput() {
		return new FieldText();
	}

	/**
	 * Returns true if this field can hold any value.
	 * @return true if this field can hold any value.
	 */
	public boolean canContainAnyValue() {
		return true;
	}

	public String getType() {
		return "Text";
	}

}