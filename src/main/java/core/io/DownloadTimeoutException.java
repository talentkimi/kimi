package core.io;


/**
 * This exception is thrown by {@link core.http.HttpDownloader} 
 * when downloading process is broken by the watchdog timer.
 * @author Dimitrijs
 * @see core.http.HttpDownloader
 * @see core.util.WatchDog
 */
public class DownloadTimeoutException extends TimeoutException {
	private static final long serialVersionUID = 8538921689570483085L;

	public DownloadTimeoutException() {
		super();
	}

	public DownloadTimeoutException(String message, Throwable cause) {
		super(message, cause);
	}

	public DownloadTimeoutException(String message) {
		super(message);
	}

	public DownloadTimeoutException(Throwable cause) {
		super(cause);
	}

}
