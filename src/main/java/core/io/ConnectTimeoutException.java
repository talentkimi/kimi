package core.io;

import java.io.IOException;

/**
 * This exception is thrown by {@link core.net.NetSocket} when connection taken too long.
 * @author Dimitrijs
 * @see core.net.NetSocket
 */
public class ConnectTimeoutException extends IOException {
	private static final long serialVersionUID = 1L;

	public ConnectTimeoutException() {
		super();
	}

	public ConnectTimeoutException(String message, Throwable cause) {
		super(message, cause);
	}

	public ConnectTimeoutException(String message) {
		super(message);
	}

	public ConnectTimeoutException(Throwable cause) {
		super(cause);
	}

}
