package core.io;


/**
 * This exception is thrown by {@link engine.webspider.WebSpider} 
 * when download time (from system variables) are over.
 * @author Dimitrijs
 * @see engine.webspider.WebSpider
 */
public class StageTimeoutException extends TimeoutException {
	private static final long serialVersionUID = 1L;

	public StageTimeoutException() {
		super();
	}

	public StageTimeoutException(String message, Throwable cause) {
		super(message, cause);
	}

	public StageTimeoutException(String message) {
		super(message);
	}

	public StageTimeoutException(Throwable cause) {
		super(cause);
	}
}
