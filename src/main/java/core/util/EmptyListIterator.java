package core.util;

import java.util.ListIterator;

public class EmptyListIterator<E> extends EmptyIterator<E> implements ListIterator<E> {

	public void add(E e) {
	}

	public boolean hasPrevious() {
		return false;
	}

	public int nextIndex() {
		return 0;
	}

	public E previous() {
		return null;
	}

	public int previousIndex() {
		return -1;
	}

	public void set(E e) {
	}

}
